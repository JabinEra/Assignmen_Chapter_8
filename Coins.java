package Problem_1;

public class Coins {

    private  double coinsValue;
    private String coinsName;

    public Coins(double value , String name){
        coinsValue = value;
        coinsName = name;
    }

    public double getCoinsValue() {
        return coinsValue;
    }

    public String getCoinsName() {
        return coinsName;
    }

}

